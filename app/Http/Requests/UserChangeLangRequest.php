<?php

namespace Nexo\Http\Requests;

use Nexo\Http\Requests\Request;

class UserChangeLangRequest extends Request
{
    /**
     * Determine if the user is authorized to make this request.
     *
     * @return bool
     */
    public function authorize()
    {
        return true;
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        return [
            'lang' => 'required'
        ];
    }
}
